PREFIXES = [
    "请问", "你好，", "我想问一下，", "请教一下，", "帮忙看看，",
    "打扰一下，", "想问下，", "请问一下，", "麻烦问一下，",
    "您好，请问", "大神请问", "求教，", "麻烦问下，",
]

SUFFIXES = [
    "谢谢！", "麻烦解答一下", "谢谢", "请帮忙看看", "感谢！",
    "求解答", "在线等", "急急急", "谢谢啦", "多谢",
]

SHORTENERS = [
    ("请问一下，", ""), ("你好，", ""), ("我想咨询一下，", ""),
    ("我想知道，", ""), ("帮忙解答一下，", ""),
]


def mutate(query, strength=1):
    """Create a natural variation of a query.
    strength=1: light modification
    strength=2: moderate
    strength=3: heavy
    """
    r = random.random()
    if strength == 1:
        if r < 0.35:
            return f"{random.choice(PREFIXES)}{query}"
        elif r < 0.55:
            return f"{query}{random.choice(SUFFIXES)}"
        else:
            return query
    elif strength == 2:
        if r < 0.25:
            return f"{random.choice(PREFIXES)}{query}"
        elif r < 0.45:
            return f"{query}{random.choice(SUFFIXES)}"
        elif r < 0.6:
            return f"{random.choice(PREFIXES)}{query}{random.choice(SUFFIXES)}"
        else:
            return query
    else:  # strength >= 3
        if r < 0.2:
            return f"{random.choice(PREFIXES)}{query}{random.choice(SUFFIXES)}"
        elif r < 0.4:
            return f"求助：{query}"
        elif r < 0.55:
            return f"问个问题——{query}"
        elif r < 0.7:
            return f"【求助】{query}"
        elif r < 0.82:
            return f"帮帮忙，{query}"
        else:
            return f"弱弱地问一句，{query}"


def dedupe(seq):
    seen = set()
    result = []
    for item in seq:
        if item not in seen:
            seen.add(item)
            result.append(item)
    return result


def expand_pool(base, target_size):
    """Expand to target_size using progressive mutation strengths."""
    base = dedupe(base)
    result = list(base)

    # Phase 1: light mutations
    for _ in range(target_size):
        if len(result) >= target_size:
            break
        q = random.choice(base)
        v = mutate(q, strength=1)
        if v not in result:
            result.append(v)

    # Phase 2: moderate mutations
    for _ in range(target_size):
        if len(result) >= target_size:
            break
        q = random.choice(base)
        v = mutate(q, strength=2)
        if v not in result:
            result.append(v)

    # Phase 3: heavy mutations
    for _ in range(target_size):
        if len(result) >= target_size:
            break
        q = random.choice(base)
        v = mutate(q, strength=3)
        if v not in result:
            result.append(v)

    random.shuffle(result)
    return result[:target_size]


def main():
    train_dir = Path(__file__).resolve().parent.parent / "Bert_2classfication" / "train_data"
    train_dir.mkdir(parents=True, exist_ok=True)

    n_train_per = 2500
    n_test_per = 250

    pro_base = dedupe(PRO)
    gen_base = dedupe(GEN)

    print(f"专业咨询基础模板: {len(pro_base)} 条（去重后）")
    print(f"通用知识基础模板: {len(gen_base)} 条（去重后）")

    # ── Training set ──
    train_pro = expand_pool(pro_base, n_train_per)
    train_gen = expand_pool(gen_base, n_train_per)

    train_records = []
    for q in train_pro:
        train_records.append({"query": q, "label": "专业咨询"})
    for q in train_gen:
        train_records.append({"query": q, "label": "通用知识"})
    random.shuffle(train_records)

    # ── Test set — zero overlap ──
    train_texts = {r["query"] for r in train_records}

    test_pro_pool = [q for q in pro_base if q not in train_texts]
    test_gen_pool = [q for q in gen_base if q not in train_texts]

    # Fill test pool with heavily mutated variants not in training
    while len(test_pro_pool) < n_test_per:
        c = mutate(random.choice(pro_base), strength=3)
        if c not in train_texts and c not in test_pro_pool:
            test_pro_pool.append(c)

    while len(test_gen_pool) < n_test_per:
        c = mutate(random.choice(gen_base), strength=3)
        if c not in train_texts and c not in test_gen_pool:
            test_gen_pool.append(c)

    random.shuffle(test_pro_pool)
    random.shuffle(test_gen_pool)
    test_pro = test_pro_pool[:n_test_per]
    test_gen = test_gen_pool[:n_test_per]

    test_records = []
    for q in test_pro:
        test_records.append({"query": q, "label": "专业咨询"})
    for q in test_gen:
        test_records.append({"query": q, "label": "通用知识"})
    random.shuffle(test_records)

    # ── Verify ──
    train_set = {r["query"] for r in train_records}
    test_set = {r["query"] for r in test_records}
    overlap = train_set & test_set
    if overlap:
        print(f"⚠️  警告: 训练集和测试集有 {len(overlap)} 条重叠")
    else:
        print("✅ 训练集和测试集零重叠")

    # ── Write ──
    train_path = train_dir / "train.jsonl"
    with train_path.open("w", encoding="utf-8") as f:
        for rec in train_records:
            f.write(json.dumps(rec, ensure_ascii=False) + "\n")

    test_path = train_dir / "test.jsonl"
    with test_path.open("w", encoding="utf-8") as f:
        for rec in test_records:
            f.write(json.dumps(rec, ensure_ascii=False) + "\n")

    # ── Summary ──
    n_train_pro = sum(1 for r in train_records if r["label"] == "专业咨询")
    n_train_gen = sum(1 for r in train_records if r["label"] == "通用知识")
    n_test_pro = sum(1 for r in test_records if r["label"] == "专业咨询")
    n_test_gen = sum(1 for r in test_records if r["label"] == "通用知识")

    # Unique count check
    unique_train = len({r["query"] for r in train_records})
    unique_test = len({r["query"] for r in test_records})

    print(f"\n训练集 (train.jsonl): {len(train_records)} 条 (唯一: {unique_train})")
    print(f"  专业咨询: {n_train_pro}")
    print(f"  通用知识: {n_train_gen}")
    print(f"测试集 (test.jsonl): {len(test_records)} 条 (唯一: {unique_test})")
    print(f"  专业咨询: {n_test_pro}")
    print(f"  通用知识: {n_test_gen}")
    print(f"\n文件保存至: {train_dir}")


if __name__ == "__main__":
    main()
